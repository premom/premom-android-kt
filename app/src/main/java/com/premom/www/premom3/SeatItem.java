package com.premom.www.premom3;

public class SeatItem {
    int fast_transfer;
    int is_seat;
    int idx;
    int car;
    int sub_car;

    public int getCar() { return car; }

    public void setCar(int car) { this.car = car; }

    public int getSub_car() { return sub_car; }

    public void setSub_car(int sub_car) { this.sub_car = sub_car; }

    public int getFast_transfer() {
        return fast_transfer;
    }

    public void setFast_transfer(int fast_transfer) {
        this.fast_transfer = fast_transfer;
    }

    public int getIs_seat() {
        return is_seat;
    }

    public void setIs_seat(int is_seat) {
        this.is_seat = is_seat;
    }

    public int getIdx() {
        return idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }


}
